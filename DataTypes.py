# String
x = "Hello"
print(x)

# Integer
x = 10
print(x)

# Float
x = 10.25
print(x)

# Complex
x = 3 + 4j
print(x)

# List
x = ["apple", "banana", "Cherry"]
print(x)

# Tuple
x = ("radha", "jay", "krishna")
print(x)

# Range
x = range(6)
print(list(x))

# Dictionary
x = {"name": "Dhanashri", "age": 36}
print(x)

# Set
x = {"apple", "banana", "cherry"}
print(x)

# Frozen Set
x = frozenset({"apple", "banana", "cherry"})
print(x)

# Boolean
x = True
print(x)

# Bytes
x = b"hello"
print(x)

# Bytearray
x = bytearray(5)
print(x)

# Memoryview
x = memoryview(bytes(5))
print(x)